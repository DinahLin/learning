package main
import "fmt"

func main() {
	// similar feature in Python
	fmt.Println("go" + "lang")
	fmt.Println("1+1=", 1+1)
	// double type setting is already taken care by the language
	fmt.Println("7.0/3.0", 7.0/3)

// comment in GO
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	// True and false logic works the same way in C
}